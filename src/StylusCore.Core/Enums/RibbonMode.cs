namespace StylusCore.Core.Enums
{
    /// <summary>
    /// Defines the ribbon/toolbar display mode.
    /// </summary>
    public enum RibbonMode
    {
        /// <summary>
        /// Full ribbon - all tabs and tools visible
        /// </summary>
        Full,

        /// <summary>
        /// Tabs only - click to expand
        /// </summary>
        TabsOnly,

        /// <summary>
        /// Hidden - Ribbon completely hidden
        /// </summary>
        Hidden
    }
}
